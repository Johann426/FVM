	SUBROUTINE P_SOURCE(IN)

	USE COMDAT_SHARED 
      
	IMPLICIT NONE
    SAVE
!------------------------------------------------------------
!   Define Argument variables
!------------------------------------------------------------
   	DOUBLE PRECISION, INTENT(IN)  :: IN(IS:IE,JS:JE,KS:KE)
	
!--------------------------------------------------------------------------------------------------------
!   PRESSURE GRADIENT
	S_U(IS1:IE1,JS1:JE1,KS1:KE1) = -0.5D0/DJAC(IS1:IE1,JS1:JE1,KS1:KE1)									&
	*(																									&
	+ DJAC(IS1+1:IE1+1,JS1:JE1,KS1:KE1)*XIX(IS1+1:IE1+1,JS1:JE1,KS1:KE1)*IN(IS1+1:IE1+1,JS1:JE1,KS1:KE1)&
	- DJAC(IS1-1:IE1-1,JS1:JE1,KS1:KE1)*XIX(IS1-1:IE1-1,JS1:JE1,KS1:KE1)*IN(IS1-1:IE1-1,JS1:JE1,KS1:KE1)&
	+ DJAC(IS1:IE1,JS1+1:JE1+1,KS1:KE1)*ETX(IS1:IE1,JS1+1:JE1+1,KS1:KE1)*IN(IS1:IE1,JS1+1:JE1+1,KS1:KE1)&
	- DJAC(IS1:IE1,JS1-1:JE1-1,KS1:KE1)*ETX(IS1:IE1,JS1-1:JE1-1,KS1:KE1)*IN(IS1:IE1,JS1-1:JE1-1,KS1:KE1)&
	+ DJAC(IS1:IE1,JS1:JE1,KS1+1:KE1+1)*ZTX(IS1:IE1,JS1:JE1,KS1+1:KE1+1)*IN(IS1:IE1,JS1:JE1,KS1+1:KE1+1)&
	- DJAC(IS1:IE1,JS1:JE1,KS1-1:KE1-1)*ZTX(IS1:IE1,JS1:JE1,KS1-1:KE1-1)*IN(IS1:IE1,JS1:JE1,KS1-1:KE1-1)&
	)

	S_V(IS1:IE1,JS1:JE1,KS1:KE1) = -0.5D0/DJAC(IS1:IE1,JS1:JE1,KS1:KE1)									&
	*(																									&
	+ DJAC(IS1+1:IE1+1,JS1:JE1,KS1:KE1)*XIY(IS1+1:IE1+1,JS1:JE1,KS1:KE1)*IN(IS1+1:IE1+1,JS1:JE1,KS1:KE1)&
	- DJAC(IS1-1:IE1-1,JS1:JE1,KS1:KE1)*XIY(IS1-1:IE1-1,JS1:JE1,KS1:KE1)*IN(IS1-1:IE1-1,JS1:JE1,KS1:KE1)&
	+ DJAC(IS1:IE1,JS1+1:JE1+1,KS1:KE1)*ETY(IS1:IE1,JS1+1:JE1+1,KS1:KE1)*IN(IS1:IE1,JS1+1:JE1+1,KS1:KE1)&
	- DJAC(IS1:IE1,JS1-1:JE1-1,KS1:KE1)*ETY(IS1:IE1,JS1-1:JE1-1,KS1:KE1)*IN(IS1:IE1,JS1-1:JE1-1,KS1:KE1)&
	+ DJAC(IS1:IE1,JS1:JE1,KS1+1:KE1+1)*ZTY(IS1:IE1,JS1:JE1,KS1+1:KE1+1)*IN(IS1:IE1,JS1:JE1,KS1+1:KE1+1)&
	- DJAC(IS1:IE1,JS1:JE1,KS1-1:KE1-1)*ZTY(IS1:IE1,JS1:JE1,KS1-1:KE1-1)*IN(IS1:IE1,JS1:JE1,KS1-1:KE1-1)&
    )
    
    S_W(IS1:IE1,JS1:JE1,KS1:KE1) = -0.5D0/DJAC(IS1:IE1,JS1:JE1,KS1:KE1)									&
	*(																									&
	+ DJAC(IS1+1:IE1+1,JS1:JE1,KS1:KE1)*XIZ(IS1+1:IE1+1,JS1:JE1,KS1:KE1)*IN(IS1+1:IE1+1,JS1:JE1,KS1:KE1)&
	- DJAC(IS1-1:IE1-1,JS1:JE1,KS1:KE1)*XIZ(IS1-1:IE1-1,JS1:JE1,KS1:KE1)*IN(IS1-1:IE1-1,JS1:JE1,KS1:KE1)&
	+ DJAC(IS1:IE1,JS1+1:JE1+1,KS1:KE1)*ETZ(IS1:IE1,JS1+1:JE1+1,KS1:KE1)*IN(IS1:IE1,JS1+1:JE1+1,KS1:KE1)&
	- DJAC(IS1:IE1,JS1-1:JE1-1,KS1:KE1)*ETZ(IS1:IE1,JS1-1:JE1-1,KS1:KE1)*IN(IS1:IE1,JS1-1:JE1-1,KS1:KE1)&
	+ DJAC(IS1:IE1,JS1:JE1,KS1+1:KE1+1)*ZTZ(IS1:IE1,JS1:JE1,KS1+1:KE1+1)*IN(IS1:IE1,JS1:JE1,KS1+1:KE1+1)&
	- DJAC(IS1:IE1,JS1:JE1,KS1-1:KE1-1)*ZTZ(IS1:IE1,JS1:JE1,KS1-1:KE1-1)*IN(IS1:IE1,JS1:JE1,KS1-1:KE1-1)&
    )
!--------------------------------------------------------------------------------------------------------
	END SUBROUTINE P_SOURCE