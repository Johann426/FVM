	SUBROUTINE ROUTINE5
	
	USE COMDAT_SHARED
	
	IMPLICIT NONE
	SAVE
	
	! SURFACE COEFFICIENTS
	
	DO RANK=0,NPRC-1

	CONTOUR = 'Surf_OO.PLT'
	
	WRITE(CONTOUR(6:7),'(I2.2)') INT(RANK)
	
	OPEN(UNIT=100,FILE=CONTOUR,STATUS='OLD')
		READ(100,*)	!CHARACTER1
		READ(100,*) !CHARACTER2
		READ(100,*) !CHARACTER3
		DO K=KS(RANK)+1,KE(RANK)-1
		DO J=JS(RANK)+1,JE(RANK)-1
		I=1
			READ(100,*) X(I,J,K),Y(I,J,K),Z(I,J,K),V1(I,J,K),V2(I,J,K),V3(I,J,K),V4(I,J,K)
		ENDDO
		ENDDO
	CLOSE(100)

	ENDDO
	
	OPEN(UNIT=100,FILE='Surf_Coef.dat',STATUS='UNKNOWN')
	WRITE(100,*) 'TITLE="SURFACE COEF."'
	WRITE(100,*) 'variables="x","y","z","Theta","Cf","Cp","Nu"'
	WRITE(100,45) 1,NJ-1,NK-1
45	FORMAT(TR1,'ZONE T="POST", I=',I6,', J=',I6,', K=',I6)
	DO K=1,NK-1
	DO J=1,NJ-1
	I=1
		WRITE(100,46) X(I,J,K),Y(I,J,K),Z(I,J,K),V1(I,J,K),V2(I,J,K),V3(I,J,K),V4(I,J,K)
46		FORMAT(TR1,D16.9,TR1,D16.9,TR1,D16.9,TR1,D16.9,TR1,D16.9,TR1,D16.9,TR1,D16.9)
	ENDDO
	ENDDO
	CLOSE(100)
	
	
	END SUBROUTINE ROUTINE5